package goldilocks;

public class Child {
	
	int weight;
	int maxTemp;
	
	public Child(int weight, int maxTemp) {
		super();
		this.weight = weight;
		this.maxTemp = maxTemp;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public int getMaxTemp() {
		return maxTemp;
	}

	public void setMaxTemp(int maxTemp) {
		this.maxTemp = maxTemp;
	}
	
	

}
